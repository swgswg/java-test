package tool;

public class AsciiChar
{
    /**
     * ASCII 转 char
     * @param i
     * @return
     */
    public static char ascii2char(int i)
    {
        return (char)i;
    }


    /**
     * char 转 ASCII
     * @param c
     * @return
     */
    public static int char2ascii(char c)
    {
        return (int)c;
    }
}
